// ── Daily series ──────────────────────────────────────────────────────────────

/** Longest gap-filled daily window rendered, to keep all-time charts legible. */
const MAX_CHART_DAYS = 180

const emptyDay = (day: string): AiUsageDaily => ({
  day,
  inputTokens: 0,
  outputTokens: 0,
  cacheReadTokens: 0,
  cacheWriteTokens: 0,
  totalTokens: 0,
  cost: 0,
})

/** Expand sparse daily rows into a contiguous series over the active range. */
function fillDaily(daily: AiUsageDaily[], range: AiUsageRange): AiUsageDaily[] {
  const today = startOfDay(new Date())
  let start: Date
  let end: Date
  if ("from" in range) {
    start = parseDay(range.from)
    end = parseDay(range.to)
  } else if (range.days > 0) {
    start = subDays(today, range.days - 1)
    end = today
  } else {
    if (daily.length === 0) return []
    start = parseDay(daily[0].day)
    end = today
  }
  const minStart = subDays(end, MAX_CHART_DAYS - 1)
  if (start < minStart) start = minStart
  if (end < start) return []

  const byDay = new Map(daily.map((d) => [d.day, d]))
  return eachDayOfInterval({ start, end }).map((date) => {
    const key = dayKey(date)
    return byDay.get(key) ?? emptyDay(key)
  })
}

// ── Overview cards ────────────────────────────────────────────────────────────

function OverviewCards({
  totals,
  daily,
}: {
  totals: AiUsageTotals
  daily: AiUsageDaily[]
}) {
  const promptTokens = totals.inputTokens + totals.cacheReadTokens
  const cacheHitRate =
    promptTokens > 0 ? (totals.cacheReadTokens / promptTokens) * 100 : 0
  const activeDays = daily.filter((d) => d.totalTokens > 0).length

  const cards = [
    {
      label: "Total tokens",
      value: formatTokens(totals.totalTokens),
      detail: `${fullFormat.format(totals.totalTokens)} tokens`,
      icon: ChartColumn,
    },
    {
      label: "Estimated cost",
      value: formatCost(totals.cost),
      detail: "Based on provider list prices",
      icon: CircleDollarSign,
    },
    {
      label: "Requests",
      value: fullFormat.format(totals.requests),
      detail: "LLM responses received",
      icon: MessagesSquare,
    },
    {
      label: "Output tokens",
      value: formatTokens(totals.outputTokens),
      detail: `${fullFormat.format(totals.outputTokens)} generated`,
      icon: ArrowDownToLine,
    },
    {
      label: "Cost per request",
      value: formatCost(
        totals.requests > 0 ? totals.cost / totals.requests : 0
      ),
      detail:
        activeDays > 0
          ? `${formatCost(totals.cost / activeDays)} per active day`
          : "No active days",
      icon: ChartPie,
    },
    {
      label: "Cache hit rate",
      value: `${cacheHitRate.toFixed(1)}%`,
      detail: "Prompt tokens served from cache",
      icon: DatabaseZap,
    },
  ]

  return (
    <div className="grid grid-cols-2 gap-3 sm:grid-cols-3">
      {cards.map((card) => (
        <div
          key={card.label}
          className="flex flex-col gap-1 rounded-lg border border-border/60 p-3"
        >
          <div className="flex items-center gap-1.5 text-muted-foreground">
            <card.icon className="size-3.5" />
            <span className="text-xs">{card.label}</span>
          </div>
          <span className="text-xl font-semibold tracking-tight tabular-nums">
            {card.value}
          </span>
          <span className="truncate text-[11px] text-muted-foreground">
            {card.detail}
          </span>
        </div>
      ))}
    </div>
  )
}

// ── Token type breakdown ──────────────────────────────────────────────────────

function TokenTypeBreakdown({ totals }: { totals: AiUsageTotals }) {
  const sum = TOKEN_TYPES.reduce((acc, t) => acc + totals[t.key], 0)

  return (
    <div className="flex flex-col gap-3 py-3.5">
      <div className="flex h-2.5 w-full overflow-hidden rounded-full bg-muted">
        {sum > 0 &&
          TOKEN_TYPES.map((t) =>
            totals[t.key] > 0 ? (
              <Tooltip key={t.key}>
                <TooltipTrigger
                  render={
                    <div
                      className={cn("h-full", t.color)}
                      style={{ width: `${(totals[t.key] / sum) * 100}%` }}
                    />
                  }
                />
                <TooltipContent>
                  {t.label}: {fullFormat.format(totals[t.key])} tokens (
                  {((totals[t.key] / sum) * 100).toFixed(1)}%)
                </TooltipContent>
              </Tooltip>
            ) : null
          )}
      </div>
      <div className="grid grid-cols-2 gap-x-6 gap-y-2 sm:grid-cols-4">
        {TOKEN_TYPES.map((t) => (
          <div key={t.key} className="flex flex-col gap-0.5">
            <div className="flex items-center gap-1.5">
              <span className={cn("size-2 rounded-full", t.color)} />
              <span className="text-xs text-muted-foreground">{t.label}</span>
            </div>
            <span className="text-sm font-medium tabular-nums">
              {formatTokens(totals[t.key])}
            </span>
            <span className="text-[11px] text-muted-foreground">
              {t.description}
            </span>
          </div>
        ))}
      </div>
    </div>
  )
}

