// ── Formatting ────────────────────────────────────────────────────────────────

const compactFormat = new Intl.NumberFormat("en-US", {
  notation: "compact",
  maximumFractionDigits: 1,
})
const fullFormat = new Intl.NumberFormat("en-US")

function formatTokens(n: number): string {
  return n >= 10_000 ? compactFormat.format(n) : fullFormat.format(n)
}

function formatCost(n: number): string {
  if (n === 0) return "$0.00"
  if (n < 0.01) return `$${n.toFixed(4)}`
  return `$${n.toFixed(2)}`
}

/** Parse a local-time YYYY-MM-DD day key into a Date. */
function parseDay(day: string): Date {
  return new Date(`${day}T00:00:00`)
}

function formatDayShort(day: string): string {
  return format(parseDay(day), "MMM d")
}

function formatDayFull(day: string): string {
  return format(parseDay(day), "EEE, MMM d, yyyy")
}

const dayKey = (date: Date) => format(date, "yyyy-MM-dd")

